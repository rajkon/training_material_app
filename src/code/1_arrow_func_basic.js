

//Example of Arrow function - with flower braces.
const cubeFunc = (num) => {
    return num * num * num;
}
console.log(cubeFunc(3));

/// Example of Arrow function - SHORT HAND
const cubeShortHandFunc = (num) => num * num * num;

console.log(cubeShortHandFunc(3));













